//Nombre de Clase
class Ejercicio4 {

    // Método Principal
    public static void main(String[] args) {

        // Declarar variable de tipo int
        int num = 7;

        // Ciclo para recorrer del 1 al 10
        for (int d = 1; d <= 10; d++) {

            // Imprimir la multiplicación de num y d
            System.out.println(num + " * " + d + " = " + (num * d));

        }
    }
}