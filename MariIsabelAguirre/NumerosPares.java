public class NumerosPares {
    //metodo main
    public static void main(String[] args) {
        // Recorremos desde 2 hasta 1000, avanzando de 2 en 2 (solo pares).
        for (int i = 2; i <= 1000; i += 2) {
            // Si el número es par, imprimimos el número
            System.out.println(i);
        }
    }
}
